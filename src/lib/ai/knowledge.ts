// Функция получения случайного сообщения по типу
export function getRandomMessage(type: string): AssistantMessage {
  const messages = knowledgeBase[type] || knowledgeBase.greeting
  const randomIndex = Math.floor(Math.random() * messages.length)
  return messages[randomIndex]
}

// Функция получения туториала (все шаги по порядку)
export function getTutorialSteps(): AssistantMessage[] {
  return knowledgeBase.tutorial
}

