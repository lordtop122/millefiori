export function findBestAnswer(question: string): string {
  const q = question.toLowerCase().trim()
  
  // Расширенный поиск по ключевым словам
  const keywordMap: Record<string, string> = {
    'как играть|правила|правило|цель': 'как играть',
    'подсказка|подскажи|помощь|помоги|не знаю': 'подсказка',
    'отмена|отменить|вернуть|назад|undo': 'отмена',
    'сложность|сложный|уровень|уровни|difficulty': 'сложность',
    'масти|масть|цвет|цвета|символы|символ|бамбук|точки|ветра|дракон': 'масти',
    'стратегия|совет|тактика|как выиграть|как лучше|план': 'стратегия',
    'рекорд|сохранить|статистика|результат|прогресс': 'рекорд',
    'привет|здравствуй|хай|хелло|hi|hello': 'привет',
    'спасибо|благодарю|thanks|спс': 'спасибо',
    'как дела|как ты|how are you': 'как дела',
    'кто ты|что ты|твоя роль': 'кто ты',
  }
  
  for (const [patterns, key] of Object.entries(keywordMap)) {
    const regex = new RegExp(patterns.split('|').join('|'), 'i')
    if (regex.test(q)) {
      return faqAnswers[key] || faqAnswers['привет']
    }
  }
  
  // Если содержит знак вопроса — даём общий ответ
  if (q.includes('?')) {
    return '🤔 Хороший вопрос! Я могу рассказать о правилах игры, дать подсказку или поделиться стратегией. Спроси конкретнее!'
  }
  
  const generalAnswers = [
    '🤔 Я могу рассказать о правилах игры, стратегии, мастях тайлов. Напиши "правила" или "стратегия"!',
    '📚 Спроси меня: "как играть", "подсказка", "стратегия", "масти" — я помогу!',
    '💭 Попробуй написать "помоги" или "что делать" — я подскажу!',
    '🎋 Напиши конкретный вопрос, например: "как найти пару" или "что такое свободный тайл".',
  ]
  
  return generalAnswers[Math.floor(Math.random() * generalAnswers.length)]
}